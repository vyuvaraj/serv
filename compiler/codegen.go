// GenerateHelpers returns utility functions needed by generated code (if/for/multi-return support).
func (c *Codegen) GenerateHelpers() string {
	return `
// isTruthy evaluates truthiness of an interface{} value.
func isTruthy(v interface{}) bool {
	if v == nil {
		return false
	}
	switch val := v.(type) {
	case bool:
		return val
	case int:
		return val != 0
	case int64:
		return val != 0
	case float64:
		return val != 0
	case string:
		return val != ""
	default:
		return true
	}
}

// toSlice coerces an interface{} to []interface{} for range iteration.
func toSlice(v interface{}) []interface{} {
	if v == nil {
		return nil
	}
	if s, ok := v.([]interface{}); ok {
		return s
	}
	return nil
}

// multiReturn splits a value into (result, error) for multi-return assignment.
// If the value is a [2]interface{} tuple, it unpacks it.
// If the value is an error, returns (nil, error).
// Otherwise returns (value, nil).
func multiReturn(v interface{}) (interface{}, interface{}) {
	if v == nil {
		return nil, nil
	}
	if tuple, ok := v.([2]interface{}); ok {
		return tuple[0], tuple[1]
	}
	if err, ok := v.(error); ok {
		return nil, err.Error()
	}
	return v, nil
}

// safeCall executes a function and catches any panic, returning (result, error).
// Used by multi-return let statements: let val, err = expr
func safeCall(fn func() interface{}) (interface{}, interface{}) {
	var result interface{}
	var errVal interface{}
	func() {
		defer func() {
			if r := recover(); r != nil {
				errVal = fmt.Sprint(r)
			}
		}()
		result = fn()
	}()
	// If the result is already a tuple (from safe runtime functions), unpack it
	if tuple, ok := result.([2]interface{}); ok {
		return tuple[0], tuple[1]
	}
	return result, errVal
}
`
}

// HasTests reports whether any test blocks were found during code generation.
func (c *Codegen) HasTests() bool {
	return len(c.testFuncs) > 0
}

// GenerateTests emits a Go _test.go file containing all collected test functions.
// The caller is responsible for writing this to a file named e.g. "serv_test.go".

func (c *Codegen) GenerateTests() string {
	var out bytes.Buffer
	out.WriteString("// Code generated by Serv compiler. DO NOT EDIT.\n")
	out.WriteString("package main\n\n")
	out.WriteString("import (\n")
	out.WriteString("\t\"fmt\"\n")
	out.WriteString("\t\"testing\"\n")
	for imp := range c.imports {
		if imp != `"testing"` && imp != `"fmt"` {
			out.WriteString("\t" + imp + "\n")
		}
	}
	out.WriteString(")\n\n")
	out.WriteString("var _ = fmt.Sprintf // keep fmt import\n")
	if c.imports[`"time"`] {
		out.WriteString("var _ = time.Second\n")
	}
	if c.imports[`"serv/runtime"`] {
		out.WriteString("var _ = runtime.Noop\n")
	}
	if c.imports[`"strconv"`] {
		out.WriteString("var _ = strconv.Atoi\n")
	}
	if c.imports[`"regexp"`] {
		out.WriteString("var _ = regexp.MustCompile\n")
	}
	out.WriteString("\n")
	for _, fn := range c.testFuncs {
		out.WriteString(fn)
		out.WriteString("\n")
	}
	return out.String()
}

// capitalizeFirst uppercases the first rune of s.
func capitalizeFirst(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}

// sanitizeTestName converts a human-readable test name (may contain spaces, dashes, etc.)
// into a valid Go identifier by title-casing each word and stripping non-alphanumeric chars.
func sanitizeTestName(name string) string {
	words := strings.FieldsFunc(name, func(r rune) bool {
		return r == ' ' || r == '-' || r == '_' || r == '.' || r == '/'
	})
	var out strings.Builder
	for _, w := range words {
		if w == "" {
			continue
		}
		out.WriteString(strings.ToUpper(w[:1]) + w[1:])
	}
	result := out.String()
	if result == "" {
		return "Unnamed"
	}
	return result
}


// genCollectionCallback generates a Go function literal for collection method callbacks.
// Handles identifiers (function references) and inline fn expressions.
func (c *Codegen) genCollectionCallback(expr Expression) (string, error) {
	// If it's a simple identifier (function name), wrap it
	if ident, ok := expr.(*Identifier); ok {
		return fmt.Sprintf("func(item interface{}) interface{} { return %s(item) }", ident.Value), nil
	}
	// If it's a call expression that looks like fn(x) { body }, it would have been parsed differently.
	// For now, generate the expression and wrap it as a callable
	exprStr, err := c.genExpression(expr)
	if err != nil {
		return "", err
	}
	return fmt.Sprintf("func(item interface{}) interface{} { return %s(item) }", exprStr), nil
}

// genReduceCallback generates a Go function literal for reduce callbacks (2 params).
func (c *Codegen) genReduceCallback(expr Expression) (string, error) {
	if ident, ok := expr.(*Identifier); ok {
		return fmt.Sprintf("func(acc interface{}, item interface{}) interface{} { return %s(acc, item) }", ident.Value), nil
	}
	exprStr, err := c.genExpression(expr)
	if err != nil {
		return "", err
	}
	return fmt.Sprintf("func(acc interface{}, item interface{}) interface{} { return %s(acc, item) }", exprStr), nil
}

func (c *Codegen) genFString(str string) (string, error) {
	var formatParts []string
	var args []string

	runes := []rune(str)
	i := 0
	n := len(runes)
	currentText := ""

	for i < n {
		if runes[i] == '{' {
			if currentText != "" {
				formatParts = append(formatParts, currentText)
				currentText = ""
			}
			i++
			exprText := ""
			for i < n && runes[i] != '}' {
				exprText += string(runes[i])
				i++
			}
			if i >= n {
				return "", fmt.Errorf("unterminated brace in f-string")
			}
			i++ // skip '}'
			formatParts = append(formatParts, "%v")

			// Parse and codegen the interpolated expression
			exprCode, err := c.compileInlineExpr(exprText)
			if err != nil {
				// Fallback to raw text if parsing fails
				args = append(args, exprText)
			} else {
				args = append(args, exprCode)
			}
		} else {
			currentText += string(runes[i])
			i++
		}
	}
	if currentText != "" {
		formatParts = append(formatParts, currentText)
	}

	formatStr := strings.Join(formatParts, "")
	if len(args) == 0 {
		return fmt.Sprintf("%q", formatStr), nil
	}
	c.imports[`"fmt"`] = true
	return fmt.Sprintf("fmt.Sprintf(%q, %s)", formatStr, strings.Join(args, ", ")), nil
}

// compileInlineExpr parses and generates Go code for a single expression string.
// Used by f-string interpolation to properly handle self.field -> self.Field etc.
func (c *Codegen) compileInlineExpr(exprText string) (string, error) {
	lexer := NewLexer(exprText)
	parser := NewParser(lexer)
	// Parse as a single expression
	expr := parser.parseExpression(LOWEST)
	if expr == nil || len(parser.Errors()) > 0 {
		return "", fmt.Errorf("failed to parse inline expression")
	}
	return c.genExpression(expr)
}

func toGoType(t string) string {
	switch t {
	case "int":
		return "int"
	case "string":
		return "string"
	case "float":
		return "float64"
	case "bool":
		return "bool"
	default:
		return "interface{}"
	}
}

func zeroValue(goType string) string {
	switch goType {
	case "int":
		return "0"
	case "float64":
		return "0.0"
	case "bool":
		return "false"
	case "string":
		return `""`
	default:
		return "nil"
	}
}

func (c *Codegen) getExpressionType(expr Expression) string {
	switch e := expr.(type) {
	case *Identifier:
		if t, ok := c.varTypes[e.Value]; ok {
			return t
		}
		return "interface{}"
	case *IntegerLiteral:
		return "int"
	case *FloatLiteral:
		return "float64"
	case *ArrayLiteral:
		return "[]interface{}"
	case *StringLiteral:
		return "string"
	case *DurationLiteral:
		return "string"
	case *BooleanLiteral:
		return "bool"
	case *NilLiteral:
		return "interface{}"
	case *StructLiteral:
		return e.TypeName
	case *SelfExpr:
		if t, ok := c.varTypes["self"]; ok {
			return t
		}
		return "interface{}"
	case *InfixExpr:
		switch e.Operator {
		case "==", "!=", "<", ">", "<=", ">=":
			return "bool"
		default:
			lt := c.getExpressionType(e.Left)
			rt := c.getExpressionType(e.Right)
			if lt == rt && (lt == "int" || lt == "float64" || lt == "string" || lt == "bool") {
				return lt
			}
			return "interface{}"
		}
	default:
		return "interface{}"
	}
}

func hasConcurrency(node Node) bool {
	if node == nil {
		return false
	}
	switch s := node.(type) {
	case *SpawnStmt, *PublishStmt:
		return true
	case *BlockStmt:
		for _, sub := range s.Statements {
			if hasConcurrency(sub) {
				return true
			}
		}
	case *TryCatchStmt:
		return hasConcurrency(s.TryBody) || hasConcurrency(s.CatchBody)
	case *MatchStmt:
		for _, cs := range s.Cases {
			if hasConcurrency(cs.Body) {
				return true
			}
		}
	case *IfStmt:
		if hasConcurrency(s.Body) {
			return true
		}
		if s.ElseBody != nil {
			return hasConcurrency(s.ElseBody)
		}
	case *ForStmt:
		return hasConcurrency(s.Body)
	case *LetStmt:
		return hasConcurrency(s.Value)
	case *ExprStmt:
		return hasConcurrency(s.Value)
	case *ReturnStmt:
		return hasConcurrency(s.Value)
	}
	return false
}

// stmtToken extracts the Token from a statement for source line tracking.
func stmtToken(stmt Statement) Token {
	switch s := stmt.(type) {
	case *LetStmt:
		return s.Token
	case *FnDecl:
		return s.Token
	case *RouteStmt:
		return s.Token
	case *EveryStmt:
		return s.Token
	case *CronStmt:
		return s.Token
	case *SubscribeStmt:
		return s.Token
	case *PublishStmt:
		return s.Token
	case *SpawnStmt:
		return s.Token
	case *ExternFnStmt:
		return s.Token
	case *BrokerStmt:
		return s.Token
	case *ServerStmt:
		return s.Token
	case *DatabaseStmt:
		return s.Token
	case *CacheStmt:
		return s.Token
	case *TryCatchStmt:
		return s.Token
	case *MatchStmt:
		return s.Token
	case *TestStmt:
		return s.Token
	case *EnumStmt:
		return s.Token
	case *ToolStmt:
		return s.Token
	case *MigrationStmt:
		return s.Token
	case *IfStmt:
		return s.Token
	case *ForStmt:
		return s.Token
	case *StructDecl:
		return s.Token
	case *MethodDecl:
		return s.Token
	case *InterfaceDecl:
		return s.Token
	case *ExportStmt:
		return stmtToken(s.Inner)
	case *ExprStmt:
		return s.Token
	case *ReturnStmt:
		return s.Token
	default:
		return Token{}
	}
}


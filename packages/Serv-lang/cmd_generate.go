func runGenerateWorkflowFromPrompt(name string, prompt string) {
	fmt.Printf("Generating workflow %s based on description: %q...\n", name, prompt)
	steps := []string{}
	delimiters := []string{"->", "then", ","}
	tempPrompt := prompt
	for _, delim := range delimiters {
		tempPrompt = strings.ReplaceAll(tempPrompt, delim, "|")
	}
	parts := strings.Split(tempPrompt, "|")
	for _, p := range parts {
		cleaned := strings.TrimSpace(p)
		if cleaned != "" {
			words := strings.Fields(cleaned)
			funcName := ""
			for idx, w := range words {
				w = strings.Title(strings.ToLower(w))
				reg := regexp.MustCompile("[^a-zA-Z0-9]")
				w = reg.ReplaceAllString(w, "")
				if idx == 0 {
					w = strings.ToLower(w)
				}
				funcName += w
			}
			if funcName != "" {
				steps = append(steps, funcName)
			}
		}
	}

	if len(steps) == 0 {
		steps = []string{"processStep1", "processStep2"}
	}

	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("// Workflow %s auto-generated from description: %q\n\n", name, prompt))
	
	for _, step := range steps {
		sb.WriteString(fmt.Sprintf("fn %s(data) {\n\t// TODO: Implement step logic\n\treturn \"ok\"\n}\n\n", step))
	}

	sb.WriteString(fmt.Sprintf("workflow %sFlow(data) {\n", name))
	for idx, step := range steps {
		sb.WriteString(fmt.Sprintf("\tlet res%d = await %s(data)\n", idx+1, step))
	}
	sb.WriteString(fmt.Sprintf("\treturn res%d\n", len(steps)))
	sb.WriteString("}\n")

	outName := strings.ToLower(name) + "_workflow.srv"
	if err := os.WriteFile(outName, []byte(sb.String()), 0644); err != nil {
		fmt.Printf("Error writing generated workflow: %v\n", err)
		os.Exit(1)
	}
	fmt.Printf("✓ Generated workflow definition at %s\n", outName)
}

func runGenerateSandbox(fileSrv string) {
	content, err := os.ReadFile(fileSrv)
	if err != nil {
		fmt.Printf("Error reading file %s: %v\n", fileSrv, err)
		os.Exit(1)
	}

	dbs := make(map[string]map[string]string)
	queues := make(map[string]string)
	storage := make(map[string]string)

	dbRegex := regexp.MustCompile(`(?s)database\s+(\w+)\s*\{[^}]*\}`)
	dbMatches := dbRegex.FindAllStringSubmatch(string(content), -1)
	for _, match := range dbMatches {
		dbName := match[1]
		dbs[dbName] = map[string]string{
			"engine":     "sqlite",
			"connection": "file:sandbox_" + strings.ToLower(dbName) + ".db?mode=memory&cache=shared",
		}
	}

	queueRegex := regexp.MustCompile(`broker:\s*\"([^\"]+)\"`)
	queueMatches := queueRegex.FindAllStringSubmatch(string(content), -1)
	for i := range queueMatches {
		queues[fmt.Sprintf("Broker%d", i+1)] = "stomp://127.0.0.1:61613"
	}
	if len(queues) == 0 && (strings.Contains(string(content), "queue") || strings.Contains(string(content), "publish")) {
		queues["DefaultBroker"] = "stomp://127.0.0.1:61613"
	}

	storeRegex := regexp.MustCompile(`store\s+(\w+)|storage\s+(\w+)`)
	if storeRegex.Match(content) {
		storage["SandboxStore"] = "file://./sandbox_store"
	}

	sandboxMap := map[string]interface{}{
		"environment": "sandbox_digital_twin",
		"databases":   dbs,
		"queues":      queues,
		"storage":     storage,
	}

	outName := "sandbox_config.json"
	for i := 0; i < len(os.Args); i++ {
		if os.Args[i] == "-o" && i+1 < len(os.Args) {
			outName = os.Args[i+1]
			break
		}
	}

	outputBytes, _ := json.MarshalIndent(sandboxMap, "", "  ")
	if err := os.WriteFile(outName, []byte(outputBytes), 0644); err != nil {
		fmt.Printf("Error writing sandbox file: %v\n", err)
		os.Exit(1)
	}
	fmt.Printf("✓ Sandbox configuration generated successfully at %s\n", outName)
}

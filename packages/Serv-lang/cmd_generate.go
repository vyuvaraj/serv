func runGenerate() {
	if len(os.Args) < 3 {
		fmt.Println("Usage:")
		fmt.Println("  serv generate client <file.srv> [--lang <typescript|python|go>] [-o <output-file>]")
		fmt.Println("  serv generate routes <spec.yaml|spec.json> [-o <output.srv>]")
		fmt.Println("  serv generate api <name>")
		fmt.Println("  serv generate db <name>")
		fmt.Println("  serv generate workflow <name>")
		fmt.Println("  serv generate \"<natural-language-prompt>\"")
		os.Exit(1)
	}

	subCommand := os.Args[2]
	switch subCommand {
	case "client":
		runGenerateClient()
	case "routes":
		runGenerateRoutes()
	case "api":
		if len(os.Args) < 4 {
			fmt.Println("Usage: serv generate api <name>")
			os.Exit(1)
		}
		runGenerateAPIScaffold(os.Args[3])
	case "db":
		if len(os.Args) < 4 {
			fmt.Println("Usage: serv generate db <name>")
			os.Exit(1)
		}
		runGenerateDBScaffold(os.Args[3])
	case "workflow":
		if len(os.Args) < 4 {
			fmt.Println("Usage: serv generate workflow <name> [--prompt <description>]")
			os.Exit(1)
		}
		prompt := ""
		for idx, arg := range os.Args {
			if arg == "--prompt" && idx+1 < len(os.Args) {
				prompt = os.Args[idx+1]
			}
		}
		if prompt != "" {
			runGenerateWorkflowFromPrompt(os.Args[3], prompt)
		} else {
			runGenerateWorkflowScaffold(os.Args[3])
		}
	case "code":
		if len(os.Args) < 4 {
			fmt.Println("Usage: serv generate code <prompt>")
			os.Exit(1)
		}
		runGenerateCodePrompt(os.Args[3])
	case "sandbox":
		if len(os.Args) < 4 {
			fmt.Println("Usage: serv generate sandbox <file.srv> [-o <output-file>]")
			os.Exit(1)
		}
		runGenerateSandbox(os.Args[3])
	default:
		runGenerateCodePrompt(subCommand)
	}
}

